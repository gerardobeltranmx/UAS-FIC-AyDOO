#include <iostream>

using namespace std;

int main()
{
    short A, B, C;
    cin >> A;
    cin >> B;
    
    C = A + B; // suma
    cout << C << endl;
    
    C = A - B; // resta
    cout << C << endl;
    
    C = A * B; // multiplicacion
    cout << C << endl;

    C = A / B; // division
    cout << C << endl;

    C = A % B; // residuo
    cout << C << endl;

    return 0;
}